// ─── User prompt builder ──────────────────────────────────────────────────────
function buildUserPrompt(projectName: string, monthLabel: string, notes: any[]): string {
  const visitBlocks = notes.map((n, i) => {
    const statusLabel = STATUS_LABEL[n.record_status ?? n.status ?? 'Normal'] ?? 'УРЕДНО (Green)'
    const weather = n.weather ? `Временски услови: ${n.weather}` : 'Временски услови: n/a'
    const rawNotes = n.notes?.trim() || '(нема белешки)'
    return [
      `--- Посета ${i + 1} | Датум: ${n.date} | Статус: ${statusLabel} ---`,
      weather,
      rawNotes,
    ].join('\n')
  }).join('\n\n')

  return `ПРОЕКТ: ${projectName}
ПЕРИОД: ${monthLabel}
ВКУПНО ТЕРЕНСКИ ПОСЕТИ: ${notes.length}

СУРОВИНИ БЕЛЕШКИ ОД ТЕРЕНСКИ ПОСЕТИ:
${visitBlocks}

Изготви го формалниот месечен извештај за надзор следејќи ја пропишаната структура со четири секции. Пиши исклучиво во проза, на македонски јазик, со целосна техничка терминологија.`
}

// ─── Route handler ────────────────────────────────────────────────────────────
export async function POST(req: Request) {
  try {
    const { notes, projectName, monthLabel } = await req.json()

    if (!notes || !Array.isArray(notes) || notes.length === 0) {
      return NextResponse.json({ error: 'No notes provided' }, { status: 400 })
    }

    const apiKey = process.env.ANTHROPIC_API_KEY
    if (!apiKey) {
      return NextResponse.json(
        { error: 'ANTHROPIC_API_KEY not configured' },
        { status: 500 }
      )
    }

    const anthropic = new Anthropic({ apiKey })

    const response = await anthropic.messages.create({
      model: 'claude-sonnet-4-20250514',
      max_tokens: 2400,
      system: SYSTEM_PROMPT,
      messages: [
        {
          role: 'user',
          content: buildUserPrompt(projectName, monthLabel, notes),
        },
      ],
    })

    const text = response.content
      .filter((b) => b.type === 'text')
      .map((b: any) => b.text)
      .join('\n')

    return NextResponse.json({ summary: text })
  } catch (err: any) {
    console.error('[summarize]', err)
    return NextResponse.json(
      { error: err?.message || 'Failed to generate summary' },
      { status: 500 }
    )
  }
}
